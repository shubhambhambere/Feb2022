package com.aspire.java.constructor;

public class Demo1 {
                      //default constructor
	                  // in default constructor no need to assign values for data types
	int a;
	float b;
	String c;
	byte D;
	long e;
	boolean f;
	
	
	public void method1()
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(D);
		System.out.println(e);
		System.out.println(f);
		
	}
	
}

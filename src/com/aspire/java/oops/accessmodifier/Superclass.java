package com.aspire.java.oops.accessmodifier;

public class Superclass {
	public void a1()
	{
		System.out.println("public from super class");
	}
	protected void a2()
	{
		System.out.println("protected from super class");
	}
	void a3()
	{
		System.out.println("Default from super class");
	}

	private void a4 ()
	{
		System.out.println("private from super class");
	}

	
}
